// AddItem inserts or upserts a single item in a manual collection. The
// caller is responsible for verifying the collection's type before invoking
// this — synced collections (mdblist/tmdb/trakt) overwrite manual edits on
// their next sync.
func (r *LibraryCollectionRepository) AddItem(ctx context.Context, collectionID, mediaItemID string, position int) error {
	tx, err := r.pool.Begin(ctx)
	if err != nil {
		return fmt.Errorf("beginning add collection item: %w", err)
	}
	defer tx.Rollback(ctx)

	if _, err := tx.Exec(ctx, `
		INSERT INTO library_collection_items (collection_id, media_item_id, position, source_rank)
		VALUES ($1, $2, $3, 0)
		ON CONFLICT (collection_id, media_item_id)
		  DO UPDATE SET position = EXCLUDED.position
	`, collectionID, mediaItemID, position); err != nil {
		return fmt.Errorf("upserting collection item: %w", err)
	}
	if _, err := tx.Exec(ctx, "UPDATE library_collections SET updated_at = NOW() WHERE id = $1", collectionID); err != nil {
		return fmt.Errorf("touching library collection: %w", err)
	}
	return tx.Commit(ctx)
}

