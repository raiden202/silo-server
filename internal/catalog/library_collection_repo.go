func normalizeCollectionLibraryIDs(legacyLibraryID int, libraryIDs []int) []int {
	if len(libraryIDs) == 0 && legacyLibraryID > 0 {
		libraryIDs = []int{legacyLibraryID}
	}
	if len(libraryIDs) == 0 {
		return nil
	}

	seen := make(map[int]struct{}, len(libraryIDs))
	normalized := make([]int, 0, len(libraryIDs))
	for _, id := range libraryIDs {
		if id <= 0 {
			continue
		}
		if _, ok := seen[id]; ok {
			continue
		}
		seen[id] = struct{}{}
		normalized = append(normalized, id)
	}
	return normalized
}

func groupKey(groupID *string) string {
	if groupID == nil {
		return ""
	}
	return *groupID
}

func compactCollectionMembershipOrder(ctx context.Context, tx pgx.Tx, libraryID int, groupID *string) error {
	_, err := tx.Exec(ctx, `
		WITH ranked AS (
		  SELECT collection_id,
		         ROW_NUMBER() OVER (ORDER BY sort_order ASC, collection_id ASC) - 1 AS pos
		  FROM library_collection_libraries
		  WHERE library_id = $1
		    AND group_id IS NOT DISTINCT FROM $2
		)
		UPDATE library_collection_libraries lcl
		SET sort_order = ranked.pos, updated_at = NOW()
		FROM ranked
		WHERE lcl.library_id = $1
		  AND lcl.collection_id = ranked.collection_id
		  AND lcl.group_id IS NOT DISTINCT FROM $2
	`, libraryID, groupID)
	if err != nil {
		return fmt.Errorf("compacting collection membership order: %w", err)
	}
	return nil
}

// ListDueForSync returns syncable collections whose next_sync_at is in the past.
// Note: in multi-node deployments, concurrent callers may receive overlapping
// sets. The in-memory inFlight guard in CollectionSyncScheduler prevents
// double-syncs within a single process.
func (r *LibraryCollectionRepository) ListDueForSync(ctx context.Context) ([]*models.LibraryCollection, error) {
	query := fmt.Sprintf(`
		SELECT %s
		FROM library_collections lc
		%s
		LEFT JOIN library_collection_items lci ON lci.collection_id = lc.id
		LEFT JOIN library_collection_libraries lcl ON lcl.collection_id = lc.id
		WHERE lc.sync_schedule IS NOT NULL
		  AND lc.next_sync_at IS NOT NULL
		  AND lc.next_sync_at <= NOW()
		  AND lc.collection_type IN ('mdblist', 'tmdb', 'trakt')
		GROUP BY lc.id, scope_lcl.group_id, scope_lcl.sort_order
		ORDER BY lc.next_sync_at ASC
	`, libraryCollectionColumns, libraryCollectionScopeFallbackJoin)

	rows, err := r.pool.Query(ctx, query)
	if err != nil {
		return nil, fmt.Errorf("listing due collections: %w", err)
	}
	defer rows.Close()

	return scanLibraryCollections(rows)
}

